package calculate

func Addition(number_a, number_b int) int {
	result := number_a + number_b
	return result
}

func Substraction(number_a, number_b int) int {
	result := number_a - number_b
	return result
}

func Multiplication(number_a, number_b int) int {
	result := number_a * number_b
	return result
}

func Division(number_a, number_b int) int {
	result := number_a / number_b
	return result
}
